let mensagem1 = document.querySelector(".mensagem-1");


function showMessage1() {
    mensagem1.style.display = "block";
}

function hideMessage1() {
    mensagem1.style.display = "none";
}

var btn1 = document.querySelector('#primeiro-botao');
var container1 = document.querySelector('.container-1');
btn1.addEventListener('click', function () {

    if (container1.style.display === 'block') {
        container1.style.display = 'none'
    } else {
        container1.style.display = 'block';
    }

});

