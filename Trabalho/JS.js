function mudaCor(el) {
    var cor = el.style.backgroundColor;
    if (cor == 'rgb(0, 0, 255)') {
        el.style.backgroundColor = '#adb5bd';
    } else {
        el.style.backgroundColor = '#adb5bd';
    }
}

